char	*ft_strrev(char *s, int len)
{
	int		i;
	char	tmp;

	i = 0;
	if (*s == '0' && *(s + 1) == '\0')
		return (s);
	while (i < len)
	{
		tmp = s[i];
		s[i] = s[len];
		s[len] = tmp;
		i++;
		len--;
	}
	return (s);
}
